DROP DATABASE IF EXISTS academy_buy_and_sell;
DROP ROLE IF EXISTS academy;

CREATE ROLE academy WITH
	LOGIN
	NOSUPERUSER
	NOCREATEDB
	NOCREATEROLE
	INHERIT
	NOREPLICATION
	CONNECTION LIMIT -1
	PASSWORD 'academy';


CREATE DATABASE academy_buy_and_sell
    WITH
    OWNER = academy
    TEMPLATE = template0
    ENCODING = 'UTF8'
    LC_COLLATE = 'C'
    LC_CTYPE = 'C'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1;

DROP TABLE IF EXISTS offers_categories;
DROP TABLE IF EXISTS categories;
DROP TABLE IF EXISTS comments;
DROP TABLE IF EXISTS offers;
DROP TABLE IF EXISTS users;

CREATE TABLE users
(
  id BIGSERIAL PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  email VARCHAR(255) UNIQUE NOT NULL,
  password VARCHAR(255) NOT NULL,
  avatar VARCHAR(50) NOT NULL
);

CREATE TABLE offers
(
    id BIGSERIAL PRIMARY KEY,
    title VARCHAR(100) NOT NULL,
    description VARCHAR(1000) NOT NULL,
    sum NUMERIC(2) NOT NULL,
    created_date DATE NOT NULL,
    picture TEXT,
    user_id BIGINT NOT NULL,
    type varchar(5) NOT NULL,
  FOREIGN KEY(user_id) REFERENCES users
    ON UPDATE CASCADE
    ON DELETE CASCADE
);


CREATE TABLE comments
(
	id BIGSERIAL PRIMARY KEY,
	text VARCHAR(1000) NOT NULL,
	created_date DATE NOT NULL,
	offer_id BIGINT NOT NULL,
	user_id BIGINT NOT NULL,
  FOREIGN KEY (offer_id) REFERENCES offers
	ON UPDATE CASCADE
	ON DELETE CASCADE,
  FOREIGN KEY (user_id) REFERENCES users
	ON UPDATE CASCADE
	ON DELETE CASCADE
);


CREATE TABLE categories
(
	id BIGSERIAL PRIMARY KEY,
	title VARCHAR(255) NOT NULL
);

CREATE TABLE offers_categories
(
  offer_id BIGINT,
  category_id BIGINT,
  CONSTRAINT offers_categories_pk PRIMARY KEY(offer_id, category_id),
  FOREIGN KEY(offer_id) REFERENCES offers
    ON UPDATE CASCADE
    ON DELETE CASCADE,
  FOREIGN KEY(category_id) REFERENCES categories
    ON UPDATE CASCADE
    ON DELETE CASCADE
);
